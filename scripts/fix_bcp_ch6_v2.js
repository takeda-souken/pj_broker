const fs = require('fs');
const bcp = JSON.parse(fs.readFileSync('data/textbook-bcp.json','utf8'));
const ch = bcp.chapters[5];

function applyFixes(sec) {
  (sec.paragraphs||[]).forEach(p => {
    if(!p.text || p.text.length <= 50 || !p.textJP || p.textJP.length / p.text.length >= 0.3) return;
    if(p.text.length >= 5000) return; // skip appendix

    if(p.num === '2.14' && p.text.includes('Fire Insurance')) {
      p.textJP = `例を示します：
(a) 火災保険
リスクの構造は何か？建物が木造であれば、コンクリート造よりもほぼ確実に危険度が高くなります。リスクの所在地はどこか？消防署からの距離や近隣の周辺リスクを判断するために重要です。建物の用途は何か？危険物は保管されているか？製造工程に危険な化学物質、可燃性物質等が関与しているか？
1月 2月 3月 4月 5月 6月 7月 8月 9月 10月 11月 12月
効力発生日 → 保険期間 → 満了日
(b) 傷害保険
傷害保険の場合、以下の情報を引き出すための質問がなされます：
▪ 職業（業務の性質または職務を含む）
▪ 年齢
▪ 健康状態
▪ 過去の傷害および疾病の履歴
▪ 身体的欠陥または虚弱等
(c) 自動車保険
運転者と自動車に関する質問がなされます。例えば、運転者の年齢、職業、運転経験、違反点数、過去3年間の請求歴、車両のメーカー・型式・年式、登録番号、座席数およびエンジン排気量、エンジン番号およびシャーシ番号等です。
申込書の質問は、付保されるリスクの物理的危険（physical hazard）と道徳的危険（moral hazard）に関する情報を引き出すために設けられています。`;
    }

    if(p.num === '3.3' && p.text.includes('Cover Note')) {
      p.textJP = `カバーノートは暫定的な保険証券と考えることができ、通常、付保されるリスクを特定するための十分な詳細を含みます：
▪ 被保険者の氏名および住所
▪ 保険金額
▪ 保険期間（通常15日間または30日間の短期間）
▪ 補償される危険
▪ 付保されるリスクの説明
▪ 番号
▪ 保証条項（warranties）
▪ 拡張補償（extensions）
▪ 保険者の権限ある署名者の署名および発行日（自動車保険等の特定の保険種類では発行時刻を含む）`;
    }

    if(p.num === '4.1' && p.text.includes('Certificate of Insurance')) {
      p.textJP = `法律により保険が義務付けられている場合、法律はまた、保険証券が有効であることを証明するために所定の保険証書（Certificate of Insurance）が発行されることを要求します。これは理にかなっています。例えば、自動車事故の場合、警察や関係者は運転者が法律を遵守し有効な保険証券を持っていることをどのように知ることができるでしょうか？運転者に実際の保険証券文書を常に携帯させることもできますが、それは煩雑です。小さな保険証書はこのような状況で有用となります。`;
    }

    if(p.num === '5.10' && p.text.includes('named perils')) {
      p.textJP = `この条項は、保険証券が何を付保しているかを正確に詳細に規定します。保険証券は「列挙危険担保（named perils / specified perils）」方式で作成される場合があり、これは保険対象の損害原因が個別に記載されていることを意味します。
以下の原因による損害を補償します：
◼ __________ ◼ __________ ◼ __________
◼ __________ ◼ __________ ◼ __________
以下を除くすべての損害リスクを補償します：
◼ _________ ◼ _________ ◼ _________
◼ _________ ◼ _________ ◼ _________
列挙危険担保（Named Perils） 対 全危険担保（All Risks）`;
    }

    if(p.num === '5.14' && p.text.includes('schedule')) {
      p.textJP = `このセクション（附則：schedule）は保険証券を保険契約者に固有のものとし、当該保険証券に関するすべての個別情報を含みます。当該保険種類のすべての保険証券に共通する標準的な情報は他のセクションに含まれます。附則は保険種類によって異なりますが、以下のデータを含みます：
▪ 保険証券番号
▪ 発行日
▪ 被保険者の氏名および住所
▪ 代理店（口座）番号
▪ 保険期間
▪ 補償される危険
▪ 被保険財物
▪ 被保険財物の所在地
▪ 保険金額（賠償責任補償の場合はてん補限度額）
▪ 適用される条項または特別規定
▪ 適用される保証条項
▪ 発行された裏書
▪ 権限ある署名`;
    }

    if(p.num === '5.23' && p.text.includes('Conditions precedent')) {
      p.textJP = `また、条件は明示的か黙示的かを問わず、以下のように分類できます：
(a) 保険証券の先行条件（Conditions precedent to the policy）——契約が有効になる前に満たされなければならない条件です。例えば、最大善意の黙示的条件は、契約に至る交渉中にすべての重要事実が開示されることを要求します。このような条件が遵守されない場合、契約全体の有効性に疑義が生じます。
(b) 保険証券の後続条件（Conditions subsequent to the policy）——契約が拘束力を持った後、契約が継続するために満たされなければならない条件です。例えば、傷害保険では、被保険者は保険期間中に職業の変更があった場合、保険者に通知することが求められます。
(c) 責任の先行条件（Conditions precedent to liability）——被保険者が契約に基づき保険金を法的に回収する権利を得る前に満たされなければならない条件です。例えば、被保険者は保険証券に基づく損害が発生した場合、所定の通知期間内に保険者に通知することが求められます。遵守を怠った場合、保険者の支払い判断および支払い額に不利な影響を及ぼす可能性があります。`;
    }

    if(!p.num && p.text.startsWith('Exclusions/ Exceptions')) {
      p.textJP = `免責事由／例外 / 条件 / 保証条項
▪ 免責事由は保険証券の補償範囲の境界を定義し制限するために使用されます
▪ 請求の状況が免責事由に該当することを示す場合、保険者は請求を否認することができます
▪ 免責事由は常に保険証券に明記され、通常、免責事由が適用されることを保険者が証明する必要があります
▪ 黙示的条件は遵守されなければならず、さもなければ契約全体の有効性に疑義が生じます
▪ 契約の先行条件は契約の成立に関するものか、継続的なものであり得ます
▪ 契約の後続条件および／または責任の先行条件は、保険者に請求を否認する権利を与えますが、契約全体を否認する権利は与えません
▪ 保証条項は厳格かつ文字通りに遵守されなければなりません
▪ 保証条項の違反は、たとえその違反が損害に結びつかなかった場合でも、保険者に否認の権利を与えます
▪ 保証条項は保険証券に書き込まれますが、黙示される場合もあります`;
    }

    if(p.num === '6.2' && p.text.includes('endorsement')) {
      p.textJP = `保険証券は、附則（schedule）および裏書（endorsement）とともに、保険契約の証拠を構成します。裏書は保険証券と一緒に読まれなければならず、特定の意味が付された語句は、登場するたびにその意味を有します。裏書の効力発生日と変更を行う裏書の文言は非常に重要です。`;
    }

    if(!p.num && p.text.startsWith('This Policy is extended to cover the Insured Person against death or disablement')) {
      p.textJP = `本保険証券は、被保険者が傷害を被り、その発生から104週間以内にその傷害が唯一の原因として以下に記載する死亡または障害（結果）を引き起こした場合で、当該傷害がテロ行為を通じて被った場合に、被保険者を補償するよう拡張されます。ただし、テロ行為が生物・化学兵器または核装置の使用を伴う場合は責任を負いません：
結果A：死亡 / 結果B：永久完全障害 / 結果C：永久部分障害 / 結果D1：一時的完全障害 / 結果D2：一時的部分障害 / 結果E：医療費 / 結果F：入院日額給付金 / 結果G：療養給付金 / 結果H：弔慰金
【見本】
上記の各結果の限度額は、当該結果に適用される規定の保険金額に基づきます。上記すべての結果の合算限度額は、本保険証券の開始日時点の結果A（死亡給付）の保険金額、またはいずれかの保険期間中に被保険者が要請し当社が同意した結果Aおよび結果Bの保険金額の増額もしくは減額が行われた場合は該当する増額・減額後の結果Aの保険金額、もしくは各被保険者あたりS$250,000のいずれか低い方までとします。
子供の被保険者に対する本拡張補償は、結果A、結果B、結果Eについてのみ適用されます。補償限度額は被保険者の結果A、結果B、結果Eの保険金額の10%です。被保険者とその法定配偶者が同時に本保険証券に基づき付保されている場合、限度額は被保険者またはその被保険配偶者の結果A、結果B、結果Eの保険金額のいずれか高い方とします。`;
    }

    if(!p.num && p.text.startsWith('This Policy is extended to cover Injury sustained by the Insured Person while on part-time National Service')) {
      p.textJP = `本保険証券は、被保険者が予備役（Reservist）として海軍、陸軍、空軍、警察、消防隊またはビジランテ隊（Vigilante Corps）でパートタイムの国民役務（National Service）に従事している間に被った傷害を補償するよう拡張されます。ただし、当該傷害が被った時に、被保険者が実際の戦闘行為、暴動、遠征、または戦争的性質の作戦に戦闘員もしくは非戦闘員として参加していた、または立ち会っていた場合、当社はいかなる補償金も支払う責任を負いません。`;
    }
  });
  (sec.subsections||[]).forEach(applyFixes);
}
ch.sections.forEach(applyFixes);

fs.writeFileSync('data/textbook-bcp.json', JSON.stringify(bcp, null, 2), 'utf8');

