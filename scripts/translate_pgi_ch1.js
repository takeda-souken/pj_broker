function applyTitles(sections) {
  if (!sections) return;
  for (const s of sections) {
    if (s.title && titleMap[s.title] && !s.titleJP) {
      s.titleJP = titleMap[s.title];
    }
    if (s.subsections) applyTitles(s.subsections);
  }
}
applyTitles(ch1.sections);

// Collect all paragraphs in order
const allParas = [];
function collect(sections) {
  if (!sections) return;
  for (const s of sections) {
    if (s.paragraphs) {
      for (const p of s.paragraphs) allParas.push(p);
    }
    if (s.subsections) collect(s.subsections);
  }
}
