// Apply fixes by section
Object.entries(fixesBySection).forEach(([secIdx, fixes]) => {
  const s = ch.sections[parseInt(secIdx)];
  function apply(sec) {
    (sec.paragraphs||[]).forEach(p => {
      if(p.num && fixes[p.num] && p.text && p.text.length > 50 && p.textJP && p.textJP.length/p.text.length < 0.3) {
        p.textJP = fixes[p.num];
      }
    });
    (sec.subsections||[]).forEach(apply);
  }
  apply(s);
});

