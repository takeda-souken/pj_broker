// Also translate section/subsection titles that haven't been translated yet
function translateTitles(sections) {
  if (!sections) return;
  for (const s of sections) {
    if (s.title && !s.titleJP) {
      const titleMap = {
        'MAS Notice 132 Notice On Cyber Hygiene': 'MAS通達132 サイバー衛生に関する通達',
        'Data Loss Protection Guidelines (DLP) For Life & General Insurance Agents': '生損保代理店向けデータ損失防止ガイドライン（DLP）',
        'Guidelines on Good Cyber Hygiene Practices': '優良サイバー衛生対策ガイドライン',
      };
      if (titleMap[s.title]) {
        s.titleJP = titleMap[s.title];
      }
    }
    if (s.subsections) translateTitles(s.subsections);
  }
}
