function collectUntranslated(sections) {
  if (!sections) return;
  for (const s of sections) {
    if (s.paragraphs) {
      for (const p of s.paragraphs) {
        if (p.textJP === p.text) {
          untranslated.push(p);
        }
      }
    }
    if (s.subsections) collectUntranslated(s.subsections);
  }
}
