function applyTitles(sections) {
  if (!sections) return;
  for (const s of sections) {
    if (s.title && titleMap[s.title] && !s.titleJP) {
      s.titleJP = titleMap[s.title];
    }
    if (s.subsections) applyTitles(s.subsections);
  }
}
