#!/usr/bin/env node
/**
 * Embed all sakura-room JSON data into sakura-room-viewer.html
 * so it works with file:// protocol (no CORS issues).
 *
 * Usage: node scripts/build-viewer-data.js
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const VIEWER = path.join(ROOT, 'sakura-room-viewer.html');
const DATA_DIR = path.join(ROOT, 'data', 'sakura-room');

const FILES = [
  'phase2-early.json', 'phase2-mid-a.json', 'phase2-late-a.json', 'phase3-a.json',
  'phase2-early-b.json', 'phase2-mid-b.json', 'phase2-late-b.json', 'phase3-b.json',
  'b-samples.json', 'c-reactions.json', 'c-samples.json',
  'd-time.json', 'd-samples.json', 'closings.json',
];

// Build data object
const data = {};
let totalConvs = 0;
FILES.forEach(f => {
  const key = 'data/sakura-room/' + f;
  const content = JSON.parse(fs.readFileSync(path.join(DATA_DIR, f), 'utf-8'));
  data[key] = content;
  const count = Array.isArray(content) ? content.length : (content.closings ? content.closings.length : 0);
  totalConvs += count;
  console.log(`  ${f.padEnd(28)} ${count} items`);
});

console.log(`  ${'─'.repeat(36)}`);
console.log(`  Total: ${totalConvs} items in ${FILES.length} files`);

// Replace the embedded-data script block in viewer HTML
let html = fs.readFileSync(VIEWER, 'utf-8');
const scriptTag = `<script id="embedded-data">
// Auto-generated by scripts/build-viewer-data.js — do not edit manually
// Generated: ${new Date().toISOString().slice(0, 19)}
window.__SAKURA_DATA = ${JSON.stringify(data)};
</script>`;

html = html.replace(
  /<script id="embedded-data">[\s\S]*?<\/script>/,
  scriptTag
);

fs.writeFileSync(VIEWER, html, 'utf-8');
console.log(`\n✓ Embedded ${FILES.length} files (${(JSON.stringify(data).length / 1024).toFixed(0)}KB) into sakura-room-viewer.html`);
